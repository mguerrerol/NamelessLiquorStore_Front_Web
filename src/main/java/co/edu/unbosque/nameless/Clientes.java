package co.edu.unbosque.nameless;

public class Clientes {

	private long cedula_clientes;
	private String direccion_clientes;
	private String email_clientes;
	private String nombre_clientes;
	private String telefono_clientes;
	
	public long getCedula_clientes() {
		return cedula_clientes;
	}
	
	public void setCedula_clientes(long cedula_clientes) {
		this.cedula_clientes = cedula_clientes;
	}
	
	public String getDireccion_clientes() {
		return direccion_clientes;
	}
	
	public void setDireccion_clientes(String direccion_clientes) {
		this.direccion_clientes = direccion_clientes;
	}
	
	public String getEmail_clientes() {
		return email_clientes;
	}
	
	public void setEmail_clientes(String email_clientes) {
		this.email_clientes = email_clientes;
	}
	
	public String getNombre_clientes() {
		return nombre_clientes;
	}
	
	public void setNombre_clientes(String nombre_clientes) {
		this.nombre_clientes = nombre_clientes;
	}
	
	public String getTelefono_clientes() {
		return telefono_clientes;
	}
	
	public void setTelefono_clientes(String telefono_clientes) {
		this.telefono_clientes = telefono_clientes;
	}
	
}
